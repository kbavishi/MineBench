          args_info->max_semphy_iter_arg = strtol (optarg, &stop_char, 0);
          if (!(stop_char && *stop_char == '\0')) {
            fprintf(stderr, "%s: invalid numeric value: %s\n", argv[0], optarg);
            goto failure;
          }
          if (args_info->max_semphy_iter_orig)
            free (args_info->max_semphy_iter_orig); /* free previous string */
          args_info->max_semphy_iter_orig = gengetopt_strdup (optarg);
          break;

        case 'J':	/* 'classic' Neighbor Joining (same as homogeneousRates).  */
